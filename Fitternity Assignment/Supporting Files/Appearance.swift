import UIKit

class Appearance {
  func setAppearance() {
    UINavigationBar.appearance().barTintColor = .white
    UINavigationBar.appearance().tintColor = .black
    UINavigationBar.appearance().backgroundColor = .white
    UINavigationBar.appearance().isTranslucent = false
  }
}
